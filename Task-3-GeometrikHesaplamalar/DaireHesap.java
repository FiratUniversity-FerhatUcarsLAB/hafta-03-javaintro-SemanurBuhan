package gorev3alanvecevrehesaplamaları;

public class DaireAlanVeCevre {
    public static void main(String[] args) {
         
           double yarıçap = 5.5;

           double pi = 3.14159;

           double alan = pi * yarıçap * yarıçap;
           
           double çevre = 2 * pi * yarıçap;

           System.out.println("Yaricap: " + yarıçap);
           System.out.println("Alan: " + alan);
           System.out.println("Cevre: " + çevre);
        
    }
    
}
